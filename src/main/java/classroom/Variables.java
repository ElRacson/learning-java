package classroom;

public class Variables {
    public static void main(String[] args) {

// One line comment

        /*Mylti-line comment */
        //
//Byte:
        byte smallNumber = 120;

        // RGB (red-green-blue)
        byte red = 120;
        byte green = 45;
        byte blue = 127;
        System.out.println(blue);

// Int Integer
        // Employee 10
        int employeeId = 123456;

        // Population count
        int populationOfLatvia = 1811956;
        System.out.println(populationOfLatvia);

        // Short
        // Game score
        short playScore = 15000;

        // Char (character)
        // Char grade
        char grade = 'A';

        // Initials
        char firstInitial = 'I';
        char lastInitial = 'R';

        // Long
        // Bank account balance:
        long accountBalance = 100000000000L;
        System.out.println(accountBalance);

        // Java 7:
        // Improves readability
        long population = 1_000_000_000L;

        // Double
        // PI
        double piValue = Math.PI;
        System.out.println(piValue);

        // Floating numbers
        // Price
        float price = 1_2345_567_8989.98F;

// String (we can save here any text in double quotes)
        String firstName = "Ilja";
        String lastName = "Racavic";

        // var
        var myCurrentAge = 27;
        var myFullName = "Ilja Racavic";

        // Boolean (true or false)
        // has or is prefix:
        boolean isSummer = false;
        boolean hasSon = false;
        boolean hasBWM = true;

        // Operators
        // * - multiplication
        // / - division
        // + - addition
        // - - subtraction
        // ++
        // --
        // %

        int remainderResult = 5 % 3;
        System.out.println(remainderResult);

        // Operators (Simple example)
        int x = 10;
        int y = 20;
        int result = x + y;

        int a = 30;
        int b = 40;
        int anotherResult = a - b;

        int c = 10 + 20;
    }
}
